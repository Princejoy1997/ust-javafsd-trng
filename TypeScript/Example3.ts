var grade:string = "A";
switch(grade){
    case "A":{
        console.log("Excellent");
        break;
    }
    case "B":{
        console.log("Fair");
        break;
    }
    case "D":{
        console.log("Poor");
        break;
    }
    default:{
        console.log("Invalid choice");
        break;
    }
}