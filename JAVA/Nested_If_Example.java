package com.ust;

 class Nested_If_Example {
	 public static void main(String args[]) {
		 int number=0;
		 
		 if(number>0) {
			 System.out.println("The number is positive.");
		 }
		 
		 else if(number<0) {
			 System.out.println("The number is neagtive.");
		 }
		 
		 else {
			 System.out.println("The number is 0");
		 }
	 }
}
